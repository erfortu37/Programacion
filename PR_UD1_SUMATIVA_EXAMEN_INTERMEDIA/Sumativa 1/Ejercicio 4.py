# Ejercicio 4 (Sumativa)

num_1=int(input("Dime un número:"))
num_2=int(input("Dime otro número:"))

div=round(num_1/num_2, 0)
exp=num_1**num_2
resto=num_1%num_2

print(f"El resultado de {num_1} dividido por {num_2} es:", div)
print(f"El resultado de {num_1} elevado a {num_2} es:", exp)
print(f"El resto de {num_1}dividido por {num_2} es:", resto)
